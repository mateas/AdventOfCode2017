    private static void reachNodes(Node node, ArrayList<Node> reachedNodes) {
        if (!reachedNodes.contains(node)){
            reachedNodes.add(node);
            for (Node adjecantNode : node.adjecantNodes) {
                reachNodes(adjecantNode, reachedNodes);
            }
        }
    }
